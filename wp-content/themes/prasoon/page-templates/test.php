<script type="text/javascript">
window.onscroll = function() {scrollFunction()};

function scrollFunction() {
	if (document.body.scrollTop > 100  || document.documentElement.scrollTop > 100) {
		document.getElementById("topnavbar").classList.add("menu-nav");
		document.getElementById("appetizers").classList.add("appetizers");
		document.getElementById("maincourse").classList.add("maincourse");
